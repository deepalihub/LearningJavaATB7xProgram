package ex_130724;

public class Lab041 {
    public static void main(String[] args) {

        // Compound assignments operators

        int a = 10;
        // *= , -= , += , /= , %=,

            a*=10;  // a = a*10;
            a-=10;  // a = a-10;
            a+=10;     // a = a+10;
            a/=10;      // a = a/10;
            a%=10;       //a = a%10;

    }
}
